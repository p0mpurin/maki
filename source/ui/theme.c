#include "theme.h"
#include <string.h>

u32 theme_bg = 0xFF281818; // ARGB format for Citro2D: C2D_Color32(0x18,0x18,0x28,0xFF)
u32 theme_sel = 0xFF3E2A2A;
u32 theme_accent = 0xFFFFA0A0;
u32 theme_text = 0xFFFFFFFF;

void theme_apply(const char *name) {
    if (strcmp(name, "blue") == 0) {
        theme_bg = C2D_Color32(0x0c,0x10,0x20,0xFF);
        theme_sel = C2D_Color32(0x1b,0x22,0x3c,0xFF);
        theme_accent = C2D_Color32(0x4d,0x90,0xfe,0xFF);
        theme_text = C2D_Color32(0xFF,0xFF,0xFF,0xFF);
    } else if (strcmp(name, "green") == 0) {
        theme_bg = C2D_Color32(0x0f,0x1a,0x14,0xFF);
        theme_sel = C2D_Color32(0x1d,0x33,0x26,0xFF);
        theme_accent = C2D_Color32(0x39,0xc5,0xbb,0xFF);
        theme_text = C2D_Color32(0xee,0xff,0xf6,0xFF);
    } else if (strcmp(name, "red") == 0) {
        theme_bg = C2D_Color32(0x1c,0x0d,0x12,0xFF);
        theme_sel = C2D_Color32(0x36,0x1c,0x24,0xFF);
        theme_accent = C2D_Color32(0xff,0x4a,0x68,0xFF);
        theme_text = C2D_Color32(0xff,0xeb,0xee,0xFF);
    } else if (strcmp(name, "pink") == 0) {
        theme_bg = C2D_Color32(0x22,0x12,0x18,0xFF);
        theme_sel = C2D_Color32(0x40,0x20,0x2c,0xFF);
        theme_accent = C2D_Color32(0xff,0xb7,0xc5,0xFF);
        theme_text = C2D_Color32(0xff,0xf0,0xf5,0xFF);
    } else { // default purple
        theme_bg = C2D_Color32(0x18,0x18,0x28,0xFF);
        theme_sel = C2D_Color32(0x2a,0x2a,0x3e,0xFF);
        theme_accent = C2D_Color32(0xa0,0xa0,0xff,0xFF);
        theme_text = C2D_Color32(0xFF,0xFF,0xFF,0xFF);
    }
}
